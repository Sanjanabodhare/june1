package practice;

public class evenodd {

	static public void main(String[] args) {
		
		int a=3;
		if(a%2==0)
		{
			System.out.println("Even number");
		}
		else
		{
			System.out.println("Odd number");
		}
		
		for(int i=1;i<10;i++)
		{
			if(i%2==0)
			{
				System.out.println(i+"even number");
			}
		}
	}

}
